// Arithmetic Operators

let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);

// Increment (++)
console.log("Incrment")

x = 10;
console.log(x);
console.log(++x);
console.log(x++);
console.log(x);


// Decrement (--)
console.log("Decrement")
x = 10;
console.log(x);
console.log(--x);
console.log(x--);
console.log(x);


